// ▼ カード名だけ検索するモジュール
const search = {
  free: (keyword) =>
    cards.filter(c =>
      c.name.includes(keyword)
    )
};

// ▼ 併用検索（キーワード × タグ × skill）
// runSearch も runFreeSearch もこれを呼ぶ
function runCombinedSearch() {
  const keyword = document.getElementById("freeSearchInput").value.trim();
  const checkedTags = [...document.querySelectorAll(".tagCheck:checked")].map(cb => cb.value);
  const skillRequired = document.getElementById("skillCheck")?.checked;

  const resultDiv = document.getElementById("result");
  resultDiv.innerHTML = "";

 if (!keyword && checkedTags.length === 0 && !skillRequired) {
  resultDiv.innerHTML = "<p>キーワードかタグを入力してください。</p>";
  return;
}


  // ① キーワード検索（カード名だけ）
  let results = keyword ? search.free(keyword) : cards;

  // ② タグ（AND検索）
  if (checkedTags.length > 0) {
    results = results.filter(card =>
      checkedTags.every(tag => card.tags.includes(tag))
    );
  }

  // ③ skill チェック
  if (skillRequired) {
    results = results.filter(card => card.skill === true);
  }

  if (results.length === 0) {
    resultDiv.innerHTML = "<p>該当するカードはありません。</p>";
    return;
  }

  // ④ 表示
  results.forEach(card => {
    const div = document.createElement("div");
    div.className = "card";

    const nameStyle = isGoldCard(card) ? "font-weight: bold;" : "";

    const formattedTags = card.tags.map(tag => {
      const isSpecial = groups.some(g =>
        g.tags.some(t => t.name === tag && t.special)
      );

      const groupClass = groups.find(g =>
        g.tags.some(t => t.name === tag)
      )?.class.replace("group-", "attr-") || "";

      const classes = `${groupClass} ${isSpecial ? "gold-attr" : ""}`.trim();

      return `<span class="${classes}">${tag}</span>`;
    }).join(", ");

    div.innerHTML = `
      <div class="card-info">
        <strong style="${nameStyle}">${card.name}</strong><br>
        特性: ${formattedTags}<br>
        大成功: ${card.skill ? "あり" : "なし"}
      </div>
      <img src="cards/${card.id}.webp" class="card-img" onerror="this.style.display='none'">
    `;

    resultDiv.appendChild(div);
  });
}

// ▼ 既存の関数を全部これに統一
