// ▼ 金枠判定
function isGoldCard(card) {
  return card.tags.some(tag =>
    groups.some(g =>
      g.tags.some(t => t.name === tag && t.special)
    )
  );
}

// ▼ AND検索
function searchCards(queryTags) {
  return cards.filter(card =>
    queryTags.every(tag => card.tags.includes(tag))
  );
}

