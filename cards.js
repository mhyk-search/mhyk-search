// ▼ チェックボックス生成
window.onload = () => {
  const tagArea = document.getElementById("tagArea");

  groups.forEach(g => {
    const title = document.createElement("div");
    title.className = `group-title ${g.class}`;
    title.textContent = g.title;
    tagArea.appendChild(title);

    const box = document.createElement("div");
    box.className = "tag-box";

    g.tags.forEach(tag => {
      const div = document.createElement("div");
      div.className = "tag-item" + (tag.special ? " special-tag" : "");
      div.innerHTML = `
        <label>
          <input type="checkbox" class="tagCheck" value="${tag.name}">
          ${tag.name}
        </label>
      `;
      box.appendChild(div);
    });

    tagArea.appendChild(box);
  });
};

